     private void cargarRolesEnTabla() {
        DefaultTableModel modelo = new DefaultTableModel();
        List<Rol> listaRoles = rolController.obtenerListaRoles();
        modelo.addColumn("ID Rol");
        modelo.addColumn("Nombre Rol");

        for (Rol rol : listaRoles) {
            modelo.addRow(new Object[]{rol.getIdrol(), rol.getNombrerol()});
        }

        TableRoles.setModel(modelo);
    }

    

     
    
    
