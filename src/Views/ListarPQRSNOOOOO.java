    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        
  int filaSeleccionada = TableRoles.getSelectedRow();

    if (filaSeleccionada == -1) {
        JOptionPane.showMessageDialog(this, "Selecciona un rol para eliminar.", "Aviso", JOptionPane.WARNING_MESSAGE);
        return;
    }

    int confirmacion = JOptionPane.showConfirmDialog(this, "¿Estás seguro de eliminar este rol?", "Confirmar eliminación", JOptionPane.YES_NO_OPTION);
    if (confirmacion != JOptionPane.YES_OPTION) {
        return;
    }

    int idRol = (int) TableRoles.getValueAt(filaSeleccionada, 0); // Asumimos que la columna 0 es el ID

    rolController.EliminarRol(idRol); // Llama al controlador para eliminar de la DB

    // Elimina de la tabla visual
    DefaultTableModel modelo = (DefaultTableModel) TableRoles.getModel();
    modelo.removeRow(filaSeleccionada);

    JOptionPane.showMessageDialog(this, "Rol eliminado correctamente.");       
        
        
    }//GEN-LAST:event_btnEliminarActionPerformed

